    public function rol(Request $request)
    {
        $user = User::findOrFail($request->get('id'));
        if ($request->get('valor') == 0) {
            $user->rol = 'user';
        } else if ($request->get('valor') == 1) {
            $user->rol = 'admin';
        }
        $user->save();
        return response()->json(['success' => 'Exito en actualizar']);
    }
